//Operações Aritméticas
 let numero1 = 20
 let numero2 = 2

 const soma = numero1 + numero2   //Essa é uma forma de fazer um cálculo

 //Adição:              +
 console.log('Soma =', soma)
 //Subtração:           -
 console.log('Subtração =', numero1 - numero2)   //Outra forma de calcular
 //Multiplicação:       *
 console.log('Multiplicação =', numero1 * numero2)
 //Divisão:             /
 console.log('Divisão =', numero1 / numero2)
 //Exponenciação:       **
 console.log('Exponenciação =', numero1 ** numero2)
 //Resto da divisão:    %
 console.log('Resto da divisão =', numero1 % numero2)


/*PRECEDENCIA DE OPERADORES
 Assim como na matemática padrão, na programação, também há precedência:
 Multiplicação, exponênciação, divisão, radificação primeiro... Depois, soma e subtração */

 console.log((3+7)*3)
 console.log((10+7+8)/3)

 //BIBLIOTECA MATH
console.log(Math.PI)
console.log(Math.sqrt(16))